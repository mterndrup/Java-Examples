package com.beans;

public class Intervals {

	private String date;
	private float kW;
	private float cost;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public float getkW() {
		return kW;
	}

	public void setKw(float kW) {
		this.kW = kW;
	}

	public float getCost() {
		return cost;
	}

	public void setCost(float cost) {
		this.cost = cost;
	}

}
